package com.pln;

public class ObjectWord {
	
	String key;
	int value;
	public ObjectWord(String k, int v){
		key = k;
		value = v;
	}
	public String getKey() {
		return key;
	}
	public void setKey(String key) {
		this.key = key;
	}
	public int getValue() {
		return value;
	}
	public void setValue(int value) {
		this.value = value;
	}
	

}
